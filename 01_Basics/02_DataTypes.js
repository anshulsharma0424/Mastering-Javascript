/*
------------------------------ NOTES ------------------------------

JavaScript has 8 basic data types:
I. Primitive Data Types:

- Number: Represents numeric values, including integers and floating-point numbers.
- String: Represents textual data, enclosed in single or double quotes.
- Boolean: Represents logical values, true or false.
- BigInt: Represents integers of arbitrary length.
- Symbol: Represents unique and immutable identifiers.
- Null (standalone value, can be assigned to a variable): Represents the intentional absence of a value. | Null is an 'Object'
- Undefined: Represents a variable that has not been assigned a value.

II. Non-primitive Data Type:

- Object: Represents collections of key-value pairs and can store complex data structures.
- Arrays: Ordered lists of values.
- Dates: Represents date and time values.
- Maps: Collections of key-value pairs where keys can be of any data type.
- Sets: Collections of unique values.

*/